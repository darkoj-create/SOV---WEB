package com.darko.speleov1

import androidx.compose.runtime.compositionLocalOf

enum class AppLanguage { HR, EN }

val LocalAppLanguage = compositionLocalOf { AppLanguage.HR }

fun appText(hr: String, en: String, language: AppLanguage): String = if (language == AppLanguage.EN) en else hr

fun AppLanguage.pick(hr: String, en: String): String = appText(hr, en, this)

fun localizedStaticLabel(value: String, language: AppLanguage): String {
    if (language == AppLanguage.HR) return value
    enStaticLabels[value]?.let { return it }
    return translateDynamicLabel(value)
}

