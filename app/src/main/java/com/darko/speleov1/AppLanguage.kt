private fun translateDynamicLabel(value: String): String {
    enStaticLabels[value]?.let { return it }
    val directlyTranslated = when {
        value.endsWith(" objekata") && value.contains(" / ") -> value.replace("objekata", "objects")
        value.endsWith(" točaka") -> value.replace("točaka", "points")
        value.endsWith(" trackova") -> value.replace("trackova", "tracks")
        value.endsWith(" stanica") -> value.replace("stanica", "stations")
        value.endsWith(" šišmiša") -> value.replace("šišmiša", "bats")
        value.startsWith("Datum: ") -> value.replaceFirst("Datum: ", "Date: ")
        value.startsWith("Datum izleta: ") -> value.replaceFirst("Datum izleta: ", "Trip date: ")
        value.startsWith("Voditelj: ") -> value.replaceFirst("Voditelj: ", "Leader: ")
        value.startsWith("Lokacija: ") -> value.replaceFirst("Lokacija: ", "Location: ")
        value.startsWith("Naziv: ") -> value.replaceFirst("Naziv: ", "Name: ")
        value.startsWith("Opis: ") -> value.replaceFirst("Opis: ", "Description: ")
        value.startsWith("Snimljeno: ") -> value.replaceFirst("Snimljeno: ", "Recorded: ")
        value.startsWith("Duljina: ") -> value.replaceFirst("Duljina: ", "Length: ")
        value.startsWith("Početak: ") -> value.replaceFirst("Početak: ", "Start: ")
        value.startsWith("Kraj: ") -> value.replaceFirst("Kraj: ", "End: ")
        value.startsWith("Ažurirano: ") -> value.replaceFirst("Ažurirano: ", "Updated: ")
        value.startsWith("Autor: ") -> value.replaceFirst("Autor: ", "Author: ")
        value.startsWith("Pločica: ") -> value.replaceFirst("Pločica: ", "Tile: ")
        value.startsWith("Županija: ") -> value.replaceFirst("Županija: ", "County: ")
        value.startsWith("Dostupna verzija: ") -> value.replaceFirst("Dostupna verzija: ", "Available version: ")
        value.startsWith("Instalirana verzija: ") -> value.replaceFirst("Instalirana verzija: ", "Installed version: ")
        value.startsWith("Prošli izleti") -> value.replaceFirst("Prošli izleti", "Past trips")
        value.startsWith("Prikazujem prvih") -> value
            .replaceFirst("Prikazujem prvih", "Showing first")
            .replace("rezultata", "results")
            .replace("mjerenja", "measurements")
            .replace("trackova", "tracks")
            .replace("layera", "layers")
        value.startsWith("Nema trackova s imenom") -> value.replaceFirst("Nema trackova s imenom", "No tracks named")
        value.startsWith("Nema mjerenja za stanicu") -> value.replaceFirst("Nema mjerenja za stanicu", "No measurements for station")
        value.startsWith("Aktivno: ") -> value.replaceFirst("Aktivno: ", "Active: ")
        value.startsWith("Format: ") -> value.replaceFirst("Format: ", "Format: ")
        value.startsWith("Prioritet: ") -> value.replaceFirst("Prioritet: ", "Priority: ")
        value.startsWith("Izlet ") -> value.replaceFirst("Izlet ", "Trip ")
        value.startsWith("SOV izlet uvezen: ") -> value.replaceFirst("SOV izlet uvezen: ", "SOV trip imported: ")
        value.startsWith("Otvaram područje izleta: ") -> value.replaceFirst("Otvaram područje izleta: ", "Opening trip area: ")
        value.startsWith("Sloj '") && value.endsWith("' obrisan.") -> value.replaceFirst("Sloj '", "Layer '").replace("' obrisan.", "' deleted.")
        value.startsWith("Otvori ") -> value.replaceFirst("Otvori ", "Open ")
        value.startsWith("Download sve pronađene") -> value.replaceFirst("Download sve pronađene", "Download all found")
        value.startsWith("Download moje custom KML točke") -> value.replaceFirst("Download moje custom KML točke", "Download my custom KML points")
        value.startsWith("Custom točke: ") -> value.replaceFirst("Custom točke: ", "Custom points: ")
        value.startsWith("GPS: ") -> value
        value.startsWith("Točke: ") -> value.replaceFirst("Točke: ", "Points: ")
        value.startsWith("Točke crteža: ") -> value.replaceFirst("Točke crteža: ", "Drawing points: ")
        value.startsWith("Automatski: ") -> value.replaceFirst("Automatski: ", "Automatic: ").replace("trackova u području", "tracks in area")
        value.startsWith("Ručno odabrano: ") -> value.replaceFirst("Ručno odabrano: ", "Manually selected: ")
        value.startsWith("Karta '") -> value.replaceFirst("Karta '", "Map '").replace("' bit će zamijenjena novom verzijom iz paketa.", "' will be replaced with the package version.")
        value.startsWith("Score ") -> value
            .replace("horizontalno", "horizontal")
            .replace("vertikala", "vertical")
            .replace("šišmiši", "bats")
        value.startsWith("#") && value.contains(" šiš.") -> value.replace("šiš.", "bats")
        value.startsWith("App će spremiti očišćeni URL") -> value.replaceFirst("App će spremiti očišćeni URL", "The app will save the cleaned URL")
        else -> null
    }
    return directlyTranslated ?: translateCroatianFragments(value)
}

